function colorDist(a: RGB, b: RGB): number {
  const dr = a[0] - b[0];
  const dg = a[1] - b[1];
  const db = a[2] - b[2];
  return dr * dr * 0.299 + dg * dg * 0.587 + db * db * 0.114;
}

export function nearestPaletteColor(r: number, g: number, b: number): RGB {
  let best: RGB = MINECRAFT_PALETTE[0];
  let bestDist = Infinity;
  for (const color of MINECRAFT_PALETTE) {
    const dist = colorDist([r, g, b], color);
    if (dist < bestDist) {
      bestDist = dist;
      best = color;
    }
  }
  return best;
}
